#include "libft.h"

int		ft_strstr_cw(const char *s1, const char *s2)
{
	size_t	i;
	size_t	j;

	j = 0;
	i = 0;
	if (s2[j] == '\0')
		return (-1);
	while (s1[i] != '\0')
	{
		j = 0;
		if (s1[i] == '\"')
		{
			i++;
			while (s1[i] != '\"' && s1[i] != '\0')
				i++;
		}
		while (s2[j] == s1[i + j] && s1[i + j] != '\0')
		{
			j++;
			if (s2[j] == '\0')
				return (i);
		}
		i++;
	}
	return (-1);
}
