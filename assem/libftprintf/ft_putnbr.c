#include "libft.h"

static void	pchar(char c)
{
	write(1, &c, 1);
}

void		ft_putnbr(int n)
{
	if (n < 0)
	{
		pchar('-');
		if (n <= -10)
			ft_putnbr(n / -10);
		pchar(-(n % 10) + '0');
	}
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		pchar((n % 10) + '0');
	}
	else
		pchar(n + '0');
}
