#include "libft.h"

int		ft_atoi(const char *a)
{
	unsigned long int	result;
	int					z;

	result = 0;
	z = 1;
	while ((*a > 8 && *a < 14) || *a == ' ')
		a++;
	if (*a == '-')
		z = -1;
	if (*a == '-' || *a == '+')
		a++;
	while (*a >= '0' && *a <= '9')
	{
		if ((result > 922337203685477580 || (result == 922337203685477580
			&& (*a - '0') > 7)) && z == 1)
			return (-1);
		else if ((result > 922337203685477580 || (result == 922337203685477580
			&& (*a - '0') > 8)) && z == -1)
			return (0);
		result = (result * 10) + (*a - '0');
		a++;
	}
	return ((int)(result * z));
}
