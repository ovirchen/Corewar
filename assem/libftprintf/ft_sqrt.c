#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb <= 0 || nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	while (i < 46340)
	{
		if (i * i == nb)
			return (i);
		i = i + 1;
	}
	return (0);
}
