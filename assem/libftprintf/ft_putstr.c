#include "libft.h"

void	ft_putstr(char const *s)
{
	int		i;
	char	a;

	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		a = s[i];
		write(1, &a, 1);
		i++;
	}
}
